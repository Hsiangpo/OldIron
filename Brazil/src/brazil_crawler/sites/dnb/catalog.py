def build_initial_segments(
    limit: int = 0,
    industry_paths: list[str] | tuple[str, ...] | None = None,
) -> list[dict[str, str | int]]:
    """生成巴西 DNB 初始切片。"""
    segments: list[dict[str, str | int]] = []
    if industry_paths:
        requested = [str(item or "").strip() for item in industry_paths if str(item or "").strip()]
        items = [slug for slug in LEGACY_SUBCATEGORY_SLUGS if slug in requested]
    else:
        items = LEGACY_SUBCATEGORY_SLUGS[: max(int(limit or 0), 0)] if int(limit or 0) > 0 else LEGACY_SUBCATEGORY_SLUGS
    for industry_path in items:
        segments.append(
            {
                "segment_id": f"{industry_path}|br||",
                "segment_type": "subcategory",
                "industry_path": industry_path,
                "country_iso_two_code": "br",
                "region_name": "",
                "city_name": "",
                "expected_count": 0,
                "next_page": 1,
                "status": "pending",
            }
        )
    return segments
